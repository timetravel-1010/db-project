        public void mostrarPeliculasAntiguas() {
        DefaultTableModel modeloDatos = (DefaultTableModel)tablaPeliAntigua.getModel();
        
        while (modeloDatos.getRowCount() > 0) {
            modeloDatos.removeRow(modeloDatos.getRowCount()-1);
        }
        List<String[]> lista = taquilla.mostrarPeliculasAntiguas();
        this.funciones = new ArrayList<>();
        
        for (int i = 0; i < lista.size(); i++) {
            String[] row =  lista.get(i);
            Object[] fila = new Object[]{row[0], row[1], row[2], row[3]};
            funciones.add(new Funcion(row[0], // idFuncion
                                      row[1], // pelicula
                                      row[2], // fecha 
                                      row[3])); // sala
            modeloDatos.addRow(fila);
        }
    }


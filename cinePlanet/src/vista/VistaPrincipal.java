    private void seleccionarSillaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_seleccionarSillaActionPerformed
        String idFuncion = comboIdFunciones.getItemAt(comboIdFunciones.getSelectedIndex());
        if (idFuncion.equalsIgnoreCase("idfuncion")) {
        	JOptionPane.showMessageDialog(null, "Por favor seleccione un idFuncion válido.");
        	return;
        }
        String tipoSilleteria = CbxTipoSilleteria.getItemAt(CbxTipoSilleteria.getSelectedIndex());
        if (tipoSilleteria.equalsIgnoreCase("seleccione")) {
        	JOptionPane.showMessageDialog(null, "Por favor seleccione un tipo de silletería válido.");
        	return;
        }
        String cantidadBoletas = txtCantidadBoletas.getText();
        int cantidadBoletasInt = Integer.parseInt(cantidadBoletas);
        if (cantidadBoletas.equalsIgnoreCase("") || cantidadBoletasInt < 1) {
        	JOptionPane.showMessageDialog(null, "Ingrese una cantidad de boletas válida.");
        	return;
        }
        else if (cantidadBoletasInt > 15) {
        	JOptionPane.showMessageDialog(null, "No puede comprar más de 15 boletas.");
        	return;
        }
        SalaCine salaCine = new SalaCine(idFuncion, tipoSilleteria, cantidadBoletas, taquilla, idUsuario, idTeatro);
        this.setEnabled(false);
        this.setVisible(false);
    }//GEN-LAST:event_seleccionarSillaActionPerformed


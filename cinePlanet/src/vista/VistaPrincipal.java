    public void mostrarPeliculas(int idTeatro) {
        DefaultTableModel modeloDatos = (DefaultTableModel)tablaPeliculas.getModel();
        
        while (modeloDatos.getRowCount() > 0) {
            modeloDatos.removeRow(modeloDatos.getRowCount()-1);
        }
        List<String[]> lista = taquilla.mostrarPeliculas(idTeatro);
        this.funciones = new ArrayList<>();
        
        comboIdFunciones.removeAllItems();
        comboIdFunciones.addItem("IdFuncion");
        
        for (int i = 0; i < lista.size(); i++) {
            String[] row =  lista.get(i);
            Object[] fila = new Object[]{row[0], row[1], row[2], row[3]};
            funciones.add(new Funcion(row[0], // idFuncion
                                      row[1], // pelicula
                                      row[2], // fecha 
                                      row[3])); // sala
            modeloDatos.addRow(fila);
            comboIdFunciones.addItem(fila[0].toString());
        }
        comboIdFunciones.setEnabled(true);
    }


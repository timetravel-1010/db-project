    public void mostrarSala(String mes){
        
        DefaultTableModel modeloDatos = (DefaultTableModel)tablaMesVenta.getModel();
        while (modeloDatos.getRowCount() > 0) {
            modeloDatos.removeRow(modeloDatos.getRowCount()-1);
        }
        
        List<String> lista = taquilla.mostrarSalaVenta(mes);
        
        if (lista.isEmpty()) return;
        Object[] fila = new Object[]{lista.get(0), lista.get(1)};
        modeloDatos.addRow(fila);
    }
    

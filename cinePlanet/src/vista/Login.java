    private void btnLogIncActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogIncActionPerformed
        // TODO add your handling code here:
        String userName, clave; 
        
        userName = txtUsername.getText();
        clave = txtClave.getText();
        
        //System.out.println(Username+" "+ clave+" ");
        resetFields();
        
        boolean validacion = taquilla.validarUsuario(userName, clave);
        
        if (validacion) {
            vista = new VistaPrincipal(taquilla, 1);
            this.setVisible(false);
        } //Pendiente generar un mensaje de login incorrecto.
        else 
            System.out.println("Usuario no valido o clave incorrecta.");    
                    
    }//GEN-LAST:event_btnLogIncActionPerformed
    
